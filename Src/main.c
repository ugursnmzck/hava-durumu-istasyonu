          rslt = bmp280_get_comp_pres_double(&pres, ucomp_data.uncomp_press, &bmp);
          basinc=pres/20000;
          //HAL_Delay(1000);


          voltaj = HAL_ADC_GetValue(&hadc1);
          voltaj = voltaj/4096*3.3;
          if (voltaj == 0){
        	  voltaj = 3.3;
          }
          else{}
          //HAL_Delay(100);

          HAL_RTC_GetTime(&hrtc , &sTime , RTC_FORMAT_BIN);
          saat   = sTime.Hours;
          dakika = sTime.Minutes;
          //HAL_Delay(250);

          sprintf(rapor,"%.0f,%.0f,%.0f,%.0f,%d,%d",sicaklik,nem,basinc,voltaj,saat,dakika);
          //HAL_Delay(100);


          sprintf (data, "AT+CIPSEND=%d,%d\r\n", 0, len);
          Uart_sendstring(data, wifi_uart);
          HAL_Delay(1000);
          while (!(Wait_for(">", wifi_uart)));
          Uart_sendstring (rapor, wifi_uart);
          HAL_Delay(1000);
          while (!(Wait_for("SEND OK", wifi_uart)));
          //HAL_Delay(1000);

